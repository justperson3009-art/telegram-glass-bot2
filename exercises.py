def get_exercise(exercise_id: str) -> dict:
    """Получить упражнение по ID"""
    return EXERCISES.get(exercise_id, {})


def get_all_exercises() -> list:
    """Получить список всех упражнений"""
    return [
        {"id": k, **v}
        for k, v in EXERCISES.items()
    ]


def format_exercise(exercise_id: str) -> str:
    """Форматировать упражнение для отправки"""
    ex = get_exercise(exercise_id)
    if not ex:
        return "❌ Упражнение не найдено"
    
    text = f"**{ex['name']}**\n\n"
    text += f"_{ex['description']}_\n\n"
    text += "**Как выполнять:**\n"
    for i, step in enumerate(ex['steps'], 1):
        text += f"{i}. {step}\n"
    text += f"\n⏱️ **Время:** {ex['duration']}"
    text += f"\n📍 **Когда:** {ex['when']}"
    
    return text
