    """Загрузка слов в базу"""
    await db.connect()
    
    # Проверяем текущее количество
    cursor = await db._connection.execute("SELECT COUNT(*) FROM words")
    count = (await cursor.fetchone())[0]
    print(f"Текущее количество слов в базе: {count}")
    
    # Добавляем слова
    added = 0
    for word in EXTENDED_WORDS:
        word = word.strip().lower()
        if word and len(word) >= 2:
            await db.add_word(word)
            added += 1
    
    # Проверяем новое количество
    cursor = await db._connection.execute("SELECT COUNT(*) FROM words")
    new_count = (await cursor.fetchone())[0]
    
    print(f"Добавлено слов: {added}")
    print(f"Новое количество слов в базе: {new_count}")
    
    await db.close()


