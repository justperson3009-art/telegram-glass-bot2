def get_random_quote() -> dict:
    """Получить случайную цитату"""
    return random.choice(QUOTES)


def get_support_message() -> str:
    """Получить сообщение поддержки"""
    return random.choice(SUPPORT_MESSAGES)


def get_quote_by_mood(mood: int) -> dict:
    """Получить цитату в зависимости от настроения"""
    if mood <= 3:
        # Низкое настроение — поддерживающие
        support_quotes = [q for q in QUOTES if "не один" in q['text'].lower() or 
                          "справишься" in q['text'].lower() or
                          "важен" in q['text'].lower()]
        return random.choice(support_quotes) if support_quotes else get_random_quote()
    elif mood <= 6:
        # Среднее — мотивирующие
        return get_random_quote()
    else:
        # Хорошее — закрепляющие
        return get_random_quote()
