class BotOpponent:
    """Бот-соперник для игры"""

    def __init__(self, difficulty: str = "medium"):
        """
        :param difficulty: уровень сложности (easy, medium, hard)
        """
        self.difficulty = difficulty
        self.known_words: List[str] = []

    def load_words(self, words: List[str]):
        """Загрузить словарь слов"""
        self.known_words = [w.lower() for w in words]

    def find_best_move(self, tiles: List[str], board: GameBoard,
                       is_first_move: bool = False) -> Optional[PlacedWord]:
        """
        Найти лучший ход
        :param tiles: буквы на руке
        :param board: игровое поле
        :param is_first_move: первый ли это ход в игре
        :return: лучший ход или None
        """
        if not self.known_words:
            # Если словарь пуст - используем базовые слова
            self._load_default_words()

        valid_moves = []

        # Перебираем слова подходящей длины
        min_len = 2 if not is_first_move else 2
        max_len = min(7, len(tiles))

        for word in self.known_words:
            if len(word) < min_len or len(word) > max_len:
                continue

            # Проверяем можно ли составить слово из букв
            if not self._can_form_word(word, tiles):
                continue

            # Пробуем разместить на поле
            move = self._find_placement(word, tiles, board, is_first_move)
            if move:
                valid_moves.append(move)

        if not valid_moves:
            return None

        # Сортируем по очкам и выбираем лучший с элементом случайности
        valid_moves.sort(key=lambda m: m.points, reverse=True)

        # Выбираем из топ-N для элемента случайности
        top_n = min(5, len(valid_moves))
        if self.difficulty == "easy":
            # Лёгкий - выбираем случайный из топ-10
            top_n = min(10, len(valid_moves))
            return random.choice(valid_moves[:top_n])
        elif self.difficulty == "hard":
            # Сложный - всегда лучший
            return valid_moves[0]
        else:
            # Средний - выбираем из топ-5
            return random.choice(valid_moves[:top_n])

    def _can_form_word(self, word: str, tiles: List[str]) -> bool:
        """Можно ли составить слово из имеющихся букв"""
        tiles_copy = tiles.copy()
        for letter in word.lower():
            if letter in tiles_copy:
                tiles_copy.remove(letter)
            else:
                return False
        return True

    def _find_placement(self, word: str, tiles: List[str], board: GameBoard,
                        is_first_move: bool) -> Optional[PlacedWord]:
        """Найти подходящее место для слова на поле"""
        # Пробуем горизонтальное и вертикальное размещение
        for horizontal in [True, False]:
            # Перебираем все возможные позиции
            for row in range(15):
                for col in range(15):
                    # Проверяем выходит ли слово за границы
                    if horizontal:
                        if col + len(word) > 15:
                            continue
                    else:
                        if row + len(word) > 15:
                            continue

                    # Пробуем разместить
                    placed = board.can_place_word(word, row, col, horizontal, tiles, is_first_move)
                    if placed:
                        return placed

        return None

